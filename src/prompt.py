from .config import FIELDS


def build_ocr_prompt() -> str:
    return (
        "Распознай весь текст и все таблицы на этой странице документа. "
        "Выведи только распознанное содержимое страницы без каких-либо пояснений, "
        "сохраняя структуру таблиц в текстовом виде."
    )


def build_extraction_system_prompt() -> str:
    fields_str = "\n".join(f"{i + 1}. {f}" for i, f in enumerate(FIELDS))
    return f"""Ты — система извлечения структурированных данных. Тебе передан текст, распознанный с отсканированного документа «Разрешение на строительство».

Извлеки значения следующих полей:
{fields_str}

Правила:
- Если поле встречается несколько раз (например, при продлении срока или внесении изменений/исправлений), верни список всех значений в хронологическом порядке: ["значение 1", "значение 2"].
- Если поле присутствует только один раз, верни строку: "значение".
- Если поле отсутствует в документе, верни null.
- Ключи ответа должны точно совпадать с названиями полей выше (включая регистр и знаки препинания).
- Верни только JSON-объект без каких-либо пояснений."""


def build_extraction_user_prompt(ocr_text: str) -> str:
    return f"Извлеки все перечисленные поля из следующего распознанного текста документа:\n\n{ocr_text}"
