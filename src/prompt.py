_HTML_TABLE_INSTRUCTIONS = """
Формат входных данных: текст получен OCR-распознаванием и содержит HTML-разметку таблиц.

Как читать HTML-таблицы:
- <table>…</table> — таблица; <tr>…</tr> — строка; <td>…</td> или <th>…</th> — ячейка.
- В двухколоночных таблицах «метка | значение» первая <td> содержит название поля, вторая <td> — его значение.
  Пример: <tr><td>2.2.1. Наименование</td><td>ООО «Ромашка»</td></tr>
  → поле «Наименование» = «ООО «Ромашка»»
- Если метка визуально занимает несколько строк, значение всё равно находится в том же <tr>.
- <br> внутри ячейки — перенос строки; читай содержимое ячейки как единый текст.
- Пустая ячейка <td></td> означает незаполненное поле — верни null.
- HTML-сущности: &nbsp; = пробел, &amp; = «&», &lt; = «<», &gt; = «>», &quot; = «"».
- Текст вне таблиц (заголовки, подписи) идёт как обычные строки.\
"""


def build_extraction_system_prompt_dynamic(fields: list[dict]) -> str:
    """Build an extraction system prompt from a user-defined field list."""
    fields_str = "\n".join(
        f"{i + 1}. {f['name']}" + (f" — {f['description']}" if f.get("description") else "")
        for i, f in enumerate(fields)
    )
    return f"""Ты — система извлечения структурированных данных. Тебе передан текст, распознанный с отсканированного документа «Разрешение на строительство».
{_HTML_TABLE_INSTRUCTIONS}
Извлеки значения следующих полей:
{fields_str}

Важные ограничения по разделам документа:
- ИНН и ОГРН/ОГРНИП застройщика: ищи ТОЛЬКО в Разделе 2 (поля 2.1.4, 2.1.5, 2.2.2, 2.2.3). Раздел 5 — это сведения о разработчике проектной документации, а не о застройщике. Данные из Раздела 5 для полей застройщика использовать НЕЛЬЗЯ.
- Наименование застройщика: Раздел 2 (поля 2.1.1–2.1.3 для физлица или 2.2.1 для организации).
- Если Раздел 2 отсутствует или все его ячейки пусты — верни null для полей застройщика.

Правила:
- Если поле присутствует только один раз, верни строку: "значение".
- Если поле встречается несколько раз, верни список всех значений в хронологическом порядке: ["значение 1", "значение 2"].
- Если поле отсутствует в документе, верни null.
- Ключи ответа должны точно совпадать с названиями полей выше (включая регистр и знаки препинания).
- Исправляй явные опечатки и артефакты OCR в извлекаемых значениях: искажённые слова (особенно записанные латиницей или транслитом), однозначно идентифицируемые как конкретное слово. Сохраняй исходное написание, если нет уверенности.
- Верни только JSON-объект без каких-либо пояснений."""


