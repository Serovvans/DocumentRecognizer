def build_classification_system_prompt() -> str:
    return """Ты — классификатор документов. Тебе будут переданы:
1. Критерий отбора пользователя
2. Список полей, которые должны присутствовать в документе
3. Распознанный текст документа

Важно: текст получен OCR-распознаванием сканированного документа и может содержать опечатки, искажения и артефакты. Оценивай документ по его общей структуре и смыслу — незначительные опечатки и ошибки распознавания не должны быть причиной отклонения.

Твоя задача — определить, соответствует ли документ критерию отбора И является ли он документом того типа, из которого можно извлечь указанные поля.
Документ считается подходящим только если выполняются оба условия.
Отвергай только если документ явно не соответствует критерию по смыслу.
Отвечай ТОЛЬКО JSON-объектом без каких-либо пояснений:
{"relevant": true, "reason": "краткое обоснование на русском"}
или
{"relevant": false, "reason": "краткое обоснование на русском"}"""


def build_classification_user_prompt(
    user_criteria: str, ocr_text: str, fields: list[dict] | None = None
) -> str:
    parts = [f"Критерий отбора: {user_criteria}"]
    if fields:
        fields_str = "\n".join(
            f"{i + 1}. {f['name']}" + (f" — {f['description']}" if f.get("description") else "")
            for i, f in enumerate(fields)
        )
        parts.append(f"Ожидаемые поля документа:\n{fields_str}")
    parts.append(f"Текст документа:\n{ocr_text}")
    return "\n\n".join(parts)


def build_single_field_system_prompt(field_name: str, field_description: str = "") -> str:
    desc_line = f"\nОписание поля: {field_description}" if field_description else ""
    return f"""Ты — система извлечения структурированных данных. Тебе передан текст, распознанный с отсканированного документа.

Извлеки значение одного поля: «{field_name}»{desc_line}

Правила:
- Если поле присутствует только один раз, верни строку: "значение".
- Если поле встречается несколько раз (например, при продлении или исправлении), верни список в хронологическом порядке: ["значение 1", "значение 2"].
- Если поле отсутствует в документе, верни null.
- Верни только JSON-объект с единственным ключом, точно совпадающим с названием поля (включая регистр и знаки препинания).
- Без каких-либо пояснений. Пример ответа: {{"{field_name}": "значение"}}"""


