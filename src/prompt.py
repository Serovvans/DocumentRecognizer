def build_classification_system_prompt() -> str:
    return """Ты — классификатор документов. Твоя единственная задача: определить, является ли переданный документ разрешением на строительство.

ВАЖНО: текст получен OCR-распознаванием отсканированного документа и ВСЕГДА содержит опечатки и артефакты. Это нормально и не является основанием для отклонения. Типичные OCR-искажения слова «строительство»: «стропельство», «стронтельство», «стропелство», «строптельство», «стропельства», «строительство» с латинскими буквами. Все эти варианты означают «строительство» — принимай их.

Признаки разрешения на строительство (достаточно ≥ 2 из них):
- Заголовки разделов вида «Раздел 1.», «Раздел 2.» и т.д.
- Нумерованные поля вида «1.1.», «1.2.», «2.1.1.» и т.д.
- Слова «разрешение на строительство» или их OCR-варианты в заголовке
- Поля: дата разрешения, номер разрешения, наименование органа, срок действия
- Разделы «Информация о застройщике», «Информация об объекте»

Безусловно ОТКЛОНЯЙ только эти типы документов (если документ явно является одним из них):
- Письмо-ответ или сопроводительное письмо (нет секционной структуры, текст в свободной форме)
- Заключение экспертизы проектной документации
- Реестр выданных разрешений (таблица со списком)
- Проектная документация (чертежи, пояснительные записки)

НЕ отклоняй документ из-за:
- Пустых или отсутствующих полей (это задача экстрактора, не классификатора)
- Опечаток OCR в любых словах
- Отсутствия данных о застройщике, ИНН, ОГРН или других полей

Отвечай ТОЛЬКО JSON-объектом без каких-либо пояснений:
{"relevant": true, "reason": "краткое обоснование на русском"}
или
{"relevant": false, "reason": "краткое обоснование на русском"}"""


def build_classification_user_prompt(
    user_criteria: str, ocr_text: str, fields: list[dict] | None = None
) -> str:
    parts = [f"Критерий отбора: {user_criteria}"]
    if fields:
        fields_str = "\n".join(
            f"{i + 1}. {f['name']}" + (f" — {f['description']}" if f.get("description") else "")
            for i, f in enumerate(fields)
        )
        parts.append(f"Ожидаемые поля документа:\n{fields_str}")
    parts.append(f"Текст документа:\n{ocr_text}")
    return "\n\n".join(parts)


